export const selectOwnerType = (response) => {
  let result = [];
  types.forEach((element) => {
    let counter = 0;
    for (let i = 0; i < element.answers.length - 1; i++) {
      if (element.answers[i] == response[i]) {
        counter++;
      }
    }
    result.push(counter);
  });

  let greatest = 0;

  for (let i = 0; i < result.length; i++) {
    if (result[i] > result[greatest]) {
      greatest = i;
    }
  }

  return types[greatest];
};
